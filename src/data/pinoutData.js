export const PINOUT_CATEGORIES = ['All', 'Microcontroller', 'Sensor', 'Display', 'Actuator', 'Communication']

export function getPinoutByCategory(category) {
  if (category === 'All') return PINOUT_DATA
  return PINOUT_DATA.filter(p => p.category === category)
}

export function searchPinouts(query) {
  if (!query.trim()) return PINOUT_DATA
  const q = query.toLowerCase()
  return PINOUT_DATA.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.category.toLowerCase().includes(q) ||
    p.pins.some(pin => pin.name.toLowerCase().includes(q))
  )
}